USE ROLE ACCOUNTADMIN;
SHOW EXTERNAL ACCESS INTEGRATIONS;

GRANT USAGE ON INTEGRATION EY_DATA_ACCESS_INTEGRATION TO ROLE PUBLIC;

CREATE OR REPLACE NETWORK RULE SNOWFLAKE_LEARNING_DB.PUBLIC.EY_DATA_NETWORK_RULE
  MODE = EGRESS
  TYPE = HOST_PORT
  VALUE_LIST = (
    'api.opentopodata.org',
    'archive-api.open-meteo.com',
    'rest.isric.org',
    'overpass-api.de',
    'data.worldpop.org',
    'figshare.com',
    'ndownloader.figshare.com'
  );

CREATE OR REPLACE EXTERNAL ACCESS INTEGRATION EY_DATA_ACCESS_INTEGRATION
  ALLOWED_NETWORK_RULES = (SNOWFLAKE_LEARNING_DB.PUBLIC.EY_DATA_NETWORK_RULE)
  ENABLED = true;

ALTER NOTEBOOK "USER$"."PUBLIC"."WATER_QUALITY_PREDICTION" 
SET EXTERNAL_ACCESS_INTEGRATIONS = ("EY_DATA_ACCESS_INTEGRATION");


